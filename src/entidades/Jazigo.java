package entidades;

public class Jazigo {

	private int codigo;
	private String rua;
	private String quadra;
	private String lado;
	private int gaveta;
	private int numero;
	private int terreno;
	private float comprimento;
	private float largura;

	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigojazigo) {
		this.codigo = codigojazigo;
	}

	public String getRua() {
		return rua;
	}

	public void setRua(String ruaJazigo) {
		this.rua = ruaJazigo;
	}

	public String getQuadra() {
		return quadra;
	}

	public void setQuadra(String quadraJazigo) {
		this.quadra = quadraJazigo;
	}

	public int getGaveta() {
		return gaveta;
	}

	public void setGaveta(int gaveta) {
		this.gaveta = gaveta;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numJazigo) {
		this.numero = numJazigo;
	}

	public String getLado() {
		return lado;
	}

	public void setLado(String lado) {
		this.lado = lado;
	}

	public int getTerreno() {
		return terreno;
	}

	public void setTerreno(int terreno) {
		this.terreno = terreno;
	}

	public float getComprimento() {
		return comprimento;
	}

	public void setComprimento(float comprimento) {
		this.comprimento = comprimento;
	}

	public float getLargura() {
		return largura;
	}

	public void setLargura(float largura) {
		this.largura = largura;
	}

	
	
	

}
